package TreeMap;

public class Student12 {

	String studentName, studentEmail;
	int studentMarks;

	public Student12(String studentName, String studentEmail, int studentMarks) {

		this.studentName = studentName;
		this.studentEmail = studentEmail;
		this.studentMarks = studentMarks;
	}

	@Override
	public String toString() {
		return "Student12 [studentName=" + studentName + ", studentEmail=" + studentEmail + ", studentMarks="
				+ studentMarks + "]";
	}

}
