package Maps;

import java.util.HashMap;

public class HashMapExample {

	public static void main(String[] args) {
		HashMap<Integer, String> hm = new HashMap<>();// It Gives Random Output

		hm.put(12, "Indrajeet");
		hm.put(121, "Akaksh");
		hm.put(54, "Ashish");
		hm.put(41, "Sushil");
		hm.put(36, "Mangesh");
		hm.put(62, "Ajit");

		System.out.println(hm + "\n");

	}

}
